/**
 * Computes the sum of two integers.
 *
 * This function returns the result of adding the two input values. It demonstrates idiomatic Kotlin syntax by omitting
 * the return type and using an expression body.
 *
 * ## Usage:
 * ```kotlin
 * val result = sum(2, 3)
 * println(result) // Output: 5
 * ```
 *
 * @param a The first integer.
 * @param b The second integer.
 * @return The sum of [a] and [b].
 */
fun sum(a: Int, b: Int) = a + b
